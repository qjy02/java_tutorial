public class Mathematics {
    public static void show() {
        double result;

        System.out.println("PI:" + Math.PI); //PI -> 3.141592653589793
        System.out.println("E:" + Math.E); //E -> 2.718281828459045
        System.out.println("Absolute: " + Math.abs(-5)); //Absolute Value -> 5
        System.out.println("Maximum: " + Math.max(1, 10)); //Max -> 10
        System.out.println("Minimum: " + Math.min(1, 10)); //Min -> 1
        System.out.println("Power: " + Math.pow(2, 10)); // Power 2^10 -> 1024
        System.out.println("SQRT: " + Math.sqrt(16)); // Square Root 16 -> 4
        System.out.println("Round: " + Math.round(3.6)); //Round 3.6 -> 4
        System.out.println("Ceiling: " + Math.ceil(3.14)); //Ceil 3.14 -> 4
        System.out.println("Floor: " + Math.floor(3.14)); //Floor 3.14 -> 3
    }
}